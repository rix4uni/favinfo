def extract_favicon_url(html_content):
    try:
        result = subprocess.run(
            ['grep', '-oP', 'href="\\K[^"]*favicon[^"]*'],
            input=html_content, text=True, capture_output=True, check=True
        )
        favicon_url = result.stdout.strip()
        return favicon_url if favicon_url else None
    except subprocess.CalledProcessError:
        return None

def fetch_url(url_pair):
    global a  # Declare a as a global variable to modify it
    original_url, url = url_pair
    try:
        ctx = ssl.create_default_context()
        ctx.check_hostname = False
        ctx.verify_mode = ssl.CERT_NONE
        response = urlopen(url, timeout=5, context=ctx)
        favicon = codecs.encode(response.read(), "base64")
        hash = mmh3.hash(favicon)
        key = hash
        a.setdefault(key, [])
        a[key].append(original_url)

        return original_url, url, hash, None
    except Exception as e:
        try:
            html_response = urlopen(original_url, timeout=5, context=ctx)
            html_content = html_response.read().decode('utf-8')
            favicon_url = extract_favicon_url(html_content)
            if favicon_url:
                if favicon_url.startswith('/'):
                    favicon_url = original_url + favicon_url
                response = urlopen(favicon_url, timeout=5, context=ctx)
                favicon = codecs.encode(response.read(), "base64")
                hash = mmh3.hash(favicon)
                key = hash
                a.setdefault(key, [])
                a[key].append(original_url)

                return original_url, favicon_url, hash, None
            else:
                return original_url, None, None, e
        except Exception as e2:
            return original_url, None, None, e

